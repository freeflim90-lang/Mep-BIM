def _expand_topics(start: int, count: int, phase: str, areas: list[str], actions: list[str]) -> list[tuple[int, str, str]]:
    """Build day topics from concrete leadership areas and learning actions."""
    topics = []
    for offset in range(count):
        area = areas[offset % len(areas)]
        action = actions[(offset // len(areas)) % len(actions)]
        topics.append((start + offset, phase, f"{area} {action}"))
    return topics


